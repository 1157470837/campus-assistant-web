package com.techthinker.CAWeb.exception;

public class GeoinfoException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2719108169232567147L;

	public GeoinfoException() {
		super();
	}

	public GeoinfoException(String message, Throwable cause) {
		super(message, cause);
	}

	public GeoinfoException(String message) {
		super(message);
	}

	public GeoinfoException(Throwable cause) {
		super(cause);
	}
}
